<?php

namespace SymfonyCollection\DotpayBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * OperationStatus
 *
 * @ORM\Table(name="dotpay_operation_status")
 * @ORM\Entity(repositoryClass="SymfonyCollection\DotpayBundle\Repository\OperationStatusRepository")
 */
class OperationStatus
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=50)
     */
    private $name;

    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return OperationType
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

}

